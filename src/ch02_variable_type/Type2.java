package ch02_variable_type;

// 실수 자료형의 표현방법
public class Type2 {
	public static void main(String[] args) {
		
		double dd1, dd2;
		dd1 = 3.141592;
		System.out.println("dd1 : " + dd1);

		dd2 = 3.14159212341234;
		System.out.println("dd2 : " + dd2);
		
		System.out.println();
		
		float ff1, ff2;
		ff1 = 3.14F;
		System.out.println("ff1 : " + ff1);
		
		ff2 = 3.14159212341234F;
		System.out.println("ff2 : " + ff2);
		
	}
}
